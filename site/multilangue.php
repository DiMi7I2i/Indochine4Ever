function afficherLibelle($key) {
	if (!empty($_SESSION['langue'])){
		if ($_SESSION['langue'] == 'fr'){
			$search = $GLOBALS['fr'];
		} else if ($_SESSION['langue'] == 'en') {
			$search = $GLOBALS['en'];
		} else {
			$search = $GLOBALS['fr'];
		}
	} else {
		$_SESSION['langue'] == 'fr';
		$search = $GLOBALS['fr'];
	}
	
	if (array_key_exists($key, $search)){
		return $search[$key];
	}
}

function choixLangue($langue){
	$_SESSION['langue'] = $langue;
}	

